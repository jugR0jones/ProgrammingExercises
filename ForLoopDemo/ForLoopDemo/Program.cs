using System.Diagnostics;

for (int i = 0; i < 10; i++)
{
    Console.WriteLine(i);
}
Console.WriteLine("----------------------");
for (int i = 0; i < 10; ++i)
{
    Console.WriteLine(i);
}
Console.WriteLine("----------------------");
for (int i = 9; i >= 0; i--)
{
    Console.WriteLine(i);
}
Console.WriteLine("----------------------");
for (int i = 9; i >= 0; --i)
{
    Console.WriteLine(i);
}
Console.WriteLine("----------------------");